// ── Per-Plugin Marketplace Record ────────────────────────────────────────────

/// In-memory projection of a plugin's marketplace state (tenant-scoped).
#[derive(Clone, Debug)]
pub struct MarketplaceRecord {
    pub plugin_id: String,
    pub state: MarketplaceState,
    pub descriptor: PluginDescriptor,
    pub manifest_hash: Option<String>,
    pub credential_ids: Vec<CredentialId>,
    pub listed_at: u64,
    pub installed_at: Option<u64>,
}

/// Per-project plugin enablement record (project-scoped).
#[derive(Clone, Debug)]
pub struct PluginEnablement {
    pub plugin_id: String,
    pub project: ProjectKey,
    pub enabled: bool,
    pub enabled_at: u64,
    pub enabled_by: OperatorId,
    pub tool_allowlist: Option<Vec<String>>,
    pub signal_allowlist: Option<Vec<String>>,
    pub signal_capture_override: Option<SignalCaptureOverride>,
}

// ── MarketplaceService ──────────────────────────────────────────────────────

/// The marketplace service sits above the plugin host and manages the
/// discover → install → credential → enable → disable → uninstall lifecycle.
pub struct MarketplaceService<S> {
    store: Arc<S>,
    /// Plugin marketplace records, keyed by plugin_id.
    records: HashMap<String, MarketplaceRecord>,
    /// Per-project enablements, keyed by (plugin_id, project).
    enablements: HashMap<(String, ProjectKey), PluginEnablement>,
}

fn now_ms() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis() as u64
}

impl<S> MarketplaceService<S> {
    pub fn new(store: Arc<S>) -> Self {
        Self {
            store,
            records: HashMap::new(),
            enablements: HashMap::new(),
        }
    }

    /// Process a marketplace command and return the resulting events.
    pub fn handle_command(
        &mut self,
        command: MarketplaceCommand,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        match command {
            MarketplaceCommand::InstallPlugin {
                plugin_id,
                initiated_by,
            } => self.handle_install(plugin_id, initiated_by),

            MarketplaceCommand::ProvidePluginCredentials {
                plugin_id,
                credentials,
                provided_by,
            } => self.handle_provide_credentials(plugin_id, credentials, provided_by),

            MarketplaceCommand::VerifyPluginCredentials {
                plugin_id,
                credential_scope_key,
                verified_by,
            } => self.handle_verify_credentials(plugin_id, credential_scope_key, verified_by),

            MarketplaceCommand::EnablePluginForProject {
                plugin_id,
                project,
                tool_allowlist,
                signal_allowlist,
                signal_capture_override,
                enabled_by,
            } => self.handle_enable(
                plugin_id,
                project,
                tool_allowlist,
                signal_allowlist,
                signal_capture_override,
                enabled_by,
            ),

            MarketplaceCommand::DisablePluginForProject {
                plugin_id,
                project,
                disabled_by,
            } => self.handle_disable(plugin_id, project, disabled_by),

            MarketplaceCommand::UninstallPlugin {
                plugin_id,
                uninstalled_by,
            } => self.handle_uninstall(plugin_id, uninstalled_by),
        }
    }

    /// Register a plugin from the catalog as Listed.
    pub fn list_plugin(&mut self, descriptor: PluginDescriptor) -> MarketplaceEvent {
        let now = now_ms();
        let plugin_id = descriptor.id.clone();
        let source = descriptor.source.clone();
        let record = MarketplaceRecord {
            plugin_id: plugin_id.clone(),
            state: MarketplaceState::Listed,
            descriptor,
            manifest_hash: None,
            credential_ids: Vec::new(),
            listed_at: now,
            installed_at: None,
        };
        self.records.insert(plugin_id.clone(), record);

        MarketplaceEvent::PluginListed {
            plugin_id,
            source,
            listed_at: now,
        }
    }

    /// Query: list all known plugin descriptors.
    pub fn list_all_records(&self) -> Vec<&MarketplaceRecord> {
        self.records.values().collect()
    }

    /// Load the bundled catalog at startup. Returns PluginListed events.
    pub fn load_bundled_catalog(&mut self) -> Vec<MarketplaceEvent> {
        let entries = match cairn_plugin_catalog::load_bundled_catalog() {
            Ok(entries) => entries,
            Err(_) => return Vec::new(),
        };

        // Collect new descriptors first to avoid borrow conflict.
        let new_descriptors: Vec<PluginDescriptor> = entries
            .into_iter()
            .filter(|e| !self.records.contains_key(&e.id))
            .map(catalog_entry_to_descriptor)
            .collect();

        new_descriptors
            .into_iter()
            .map(|desc| self.list_plugin(desc))
            .collect()
    }

    /// Query: is a plugin installed and available?
    pub fn get_record(&self, plugin_id: &str) -> Option<&MarketplaceRecord> {
        self.records.get(plugin_id)
    }

    /// Query: is a plugin enabled for a project?
    pub fn get_enablement(
        &self,
        plugin_id: &str,
        project: &ProjectKey,
    ) -> Option<&PluginEnablement> {
        self.enablements
            .get(&(plugin_id.to_string(), project.clone()))
    }

    /// Query: list all enablements for a project.
    pub fn enablements_for_project(&self, project: &ProjectKey) -> Vec<&PluginEnablement> {
        self.enablements
            .values()
            .filter(|e| &e.project == project && e.enabled)
            .collect()
    }

    /// Build a VisibilityContext for a run in a project.
    ///
    /// This is used by prompt building and tool_search to filter tools
    /// to only those from plugins enabled for the project.
    pub fn build_visibility_context(
        &self,
        project: &ProjectKey,
        run_id: Option<RunId>,
    ) -> VisibilityContext {
        let enablements = self.enablements_for_project(project);

        let enabled_plugins: HashSet<String> =
            enablements.iter().map(|e| e.plugin_id.clone()).collect();

        let allowlisted_tools: HashMap<String, Option<HashSet<String>>> = enablements
            .iter()
            .map(|e| {
                let tools = e
                    .tool_allowlist
                    .as_ref()
                    .map(|list| list.iter().cloned().collect::<HashSet<String>>());
                (e.plugin_id.clone(), tools)
            })
            .collect();

        VisibilityContext {
            project: project.clone(),
            run_id,
            enabled_plugins,
            allowlisted_tools,
        }
    }

    // ── Command Handlers ─────────────────────────────────────────────────

    fn handle_install(
        &mut self,
        plugin_id: String,
        initiated_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let record = self
            .records
            .get(&plugin_id)
            .ok_or_else(|| MarketplaceError::PluginNotFound(plugin_id.clone()))?;

        // RFC 015 Non-Goals: reject manifests declaring eval_scorer capability.
        if record.descriptor.category == PluginCategory::EvalScorer {
            return Err(MarketplaceError::EvalScorerReserved);
        }

        match &record.state {
            MarketplaceState::Listed | MarketplaceState::InstallationFailed { .. } => {}
            MarketplaceState::Uninstalled => {}
            _ => {
                return Err(MarketplaceError::InvalidTransition {
                    plugin_id: plugin_id.clone(),
                    from: format!("{:?}", record.state),
                    to: "Installing".to_string(),
                });
            }
        }

        let now = now_ms();
        let started = MarketplaceEvent::PluginInstallationStarted {
            plugin_id: plugin_id.clone(),
            initiated_by,
            started_at: now,
        };

        // Transition to Installing
        if let Some(record) = self.records.get_mut(&plugin_id) {
            record.state = MarketplaceState::Installing;
        }

        // For now, immediately mark as installed (real impl will do async download)
        let installed = MarketplaceEvent::PluginInstalled {
            plugin_id: plugin_id.clone(),
            manifest_hash: String::new(), // populated during real install
            at: now,
        };

        if let Some(record) = self.records.get_mut(&plugin_id) {
            record.state = MarketplaceState::Installed;
            record.installed_at = Some(now);
        }

        Ok(vec![started, installed])
    }

    fn handle_provide_credentials(
        &mut self,
        plugin_id: String,
        _credentials: Vec<(String, CredentialValue)>,
        provided_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let record = self
            .records
            .get(&plugin_id)
            .ok_or_else(|| MarketplaceError::PluginNotFound(plugin_id.clone()))?;

        if record.state != MarketplaceState::Installed {
            return Err(MarketplaceError::InvalidTransition {
                plugin_id: plugin_id.clone(),
                from: format!("{:?}", record.state),
                to: "CredentialsProvided".to_string(),
            });
        }

        let now = now_ms();
        // Real impl: validate credential shape, store via CredentialService
        let event = MarketplaceEvent::PluginCredentialsProvided {
            plugin_id,
            credential_ids: Vec::new(), // populated by CredentialService
            provided_by,
            at: now,
        };
        Ok(vec![event])
    }

    fn handle_verify_credentials(
        &self,
        plugin_id: String,
        credential_scope_key: Option<CredentialScopeKey>,
        verified_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let record = self
            .records
            .get(&plugin_id)
            .ok_or_else(|| MarketplaceError::PluginNotFound(plugin_id.clone()))?;

        if record.state != MarketplaceState::Installed {
            return Err(MarketplaceError::PluginNotInstalled(plugin_id.clone()));
        }

        let now = now_ms();
        let scope_key =
            credential_scope_key.unwrap_or_else(|| CredentialScopeKey("tenant-default".into()));

        // Real impl: spawns a transient process, runs health check, shuts down.
        // This is an ephemeral action — no persistent state change.
        let event = MarketplaceEvent::PluginCredentialsVerified {
            plugin_id,
            credential_scope_key: scope_key,
            outcome: VerificationOutcome::Ok,
            verified_by,
            at: now,
        };
        Ok(vec![event])
    }

    fn handle_enable(
        &mut self,
        plugin_id: String,
        project: ProjectKey,
        tool_allowlist: Option<Vec<String>>,
        signal_allowlist: Option<Vec<String>>,
        signal_capture_override: Option<SignalCaptureOverride>,
        enabled_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let record = self
            .records
            .get(&plugin_id)
            .ok_or_else(|| MarketplaceError::PluginNotFound(plugin_id.clone()))?;

        if record.state != MarketplaceState::Installed {
            return Err(MarketplaceError::PluginNotInstalled(plugin_id.clone()));
        }

        let now = now_ms();
        let enablement = PluginEnablement {
            plugin_id: plugin_id.clone(),
            project: project.clone(),
            enabled: true,
            enabled_at: now,
            enabled_by: enabled_by.clone(),
            tool_allowlist: tool_allowlist.clone(),
            signal_allowlist: signal_allowlist.clone(),
            signal_capture_override: signal_capture_override.clone(),
        };
        self.enablements
            .insert((plugin_id.clone(), project.clone()), enablement);

        let event = MarketplaceEvent::PluginEnabledForProject {
            plugin_id,
            project,
            enabled_by,
            tool_allowlist,
            signal_allowlist,
            signal_capture_override,
            at: now,
        };
        Ok(vec![event])
    }

    fn handle_disable(
        &mut self,
        plugin_id: String,
        project: ProjectKey,
        disabled_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let key = (plugin_id.clone(), project.clone());
        let enablement = self.enablements.get_mut(&key).ok_or_else(|| {
            MarketplaceError::NotEnabledForProject {
                plugin_id: plugin_id.clone(),
                project: project.clone(),
            }
        })?;

        enablement.enabled = false;

        let now = now_ms();
        let event = MarketplaceEvent::PluginDisabledForProject {
            plugin_id,
            project,
            disabled_by,
            at: now,
        };
        Ok(vec![event])
    }

    fn handle_uninstall(
        &mut self,
        plugin_id: String,
        uninstalled_by: OperatorId,
    ) -> Result<Vec<MarketplaceEvent>, MarketplaceError> {
        let record = self
            .records
            .get(&plugin_id)
            .ok_or_else(|| MarketplaceError::PluginNotFound(plugin_id.clone()))?;

        if record.state == MarketplaceState::Listed {
            return Err(MarketplaceError::InvalidTransition {
                plugin_id: plugin_id.clone(),
                from: "Listed".to_string(),
                to: "Uninstalled".to_string(),
            });
        }

        let now = now_ms();
        let credentials_revoked = record.credential_ids.clone();

        // Disable all project enablements
        let affected_keys: Vec<_> = self
            .enablements
            .keys()
            .filter(|(pid, _)| pid == &plugin_id)
            .cloned()
            .collect();
        for key in affected_keys {
            self.enablements.remove(&key);
        }

        // Transition to Uninstalled
        if let Some(record) = self.records.get_mut(&plugin_id) {
            record.state = MarketplaceState::Uninstalled;
        }

        let event = MarketplaceEvent::PluginUninstalled {
            plugin_id,
            uninstalled_by,
            credentials_revoked,
            at: now,
        };
        Ok(vec![event])
    }
}

// ── Catalog Conversion ──────────────────────────────────────────────────────

/// Convert a catalog TOML entry into a full PluginDescriptor.
pub fn catalog_entry_to_descriptor(entry: CatalogEntry) -> PluginDescriptor {
    let category = match entry.category.as_str() {
        "issue_tracker" => PluginCategory::IssueTracker,
        "chat_ops" => PluginCategory::ChatOps,
        "calendar" => PluginCategory::Calendar,
        "files" => PluginCategory::Files,
        "customer_support" => PluginCategory::CustomerSupport,
        "observability" => PluginCategory::Observability,
        "data_source" => PluginCategory::DataSource,
        "communication_channel" => PluginCategory::CommunicationChannel,
        "eval_scorer" => PluginCategory::EvalScorer,
        other => PluginCategory::Other(other.to_string()),
    };

    let required_credentials = entry
        .required_credentials
        .into_iter()
        .map(|c| CredentialSpec {
            key: c.key,
            display_name: c.display_name,
            kind: match c.kind.as_str() {
                "oauth2" => CredentialKind::OAuth2,
                "api_key" => CredentialKind::ApiKey,
                "app_installation" => CredentialKind::AppInstallation,
                "basic_auth" => CredentialKind::BasicAuth,
                _ => CredentialKind::ApiKey,
            },
            help_url: c.help_url,
            validation: None,
            scope: match c.scope.as_str() {
                "tenant" => CredentialScopeHint::Tenant,
                "workspace" => CredentialScopeHint::Workspace,
                "project" => CredentialScopeHint::Project,
                _ => CredentialScopeHint::Tenant,
            },
            generated: c.generated,
        })
        .collect();

    let post_install_health_check = entry.health_check.map(|hc| HealthCheckSpec {
        method: hc.method,
        timeout_ms: hc.timeout_ms,
        success_criteria: serde_json::Value::Null,
    });

    PluginDescriptor {
        id: entry.id,
        name: entry.name,
        version: entry.version,
        description: entry.description,
        category,
        icon_url: entry.icon_url,
        vendor: entry.vendor,
        command: entry.command,
        tools: entry.tools,
        signal_sources: entry.signal_sources,
        channels: entry.channels,
        required_credentials,
        required_network_egress: entry.network_egress,
        post_install_health_check,
        source: DescriptorSource::BundledCatalog,
        download_url: entry.download_url,
        has_signal_source: entry.has_signal_source,
    }
}

// ── Visibility Filtering (RFC 015 §"Per-Run Tool Visibility") ────────────────

/// Check whether a plugin tool is visible in the given VisibilityContext.
///
/// A tool is visible if:
/// 1. Its plugin is in the context's enabled_plugins set
/// 2. Either the plugin has no tool allowlist (None = all tools visible)
///    or the tool name is in the allowlist
///
/// Built-in cairn tools (not from plugins) are always visible — this function
/// only gates plugin-provided tools.
pub fn is_plugin_tool_visible(ctx: &VisibilityContext, plugin_id: &str, tool_name: &str) -> bool {
    if !ctx.enabled_plugins.contains(plugin_id) {
        return false;
    }
    match ctx.allowlisted_tools.get(plugin_id) {
        Some(Some(allowed)) => allowed.contains(tool_name),
        Some(None) => true, // None = all tools allowed
        None => false,      // plugin not in allowlist map at all
    }
}

/// Check whether a signal type is allowed for a project's enablement.
///
/// A signal type passes if the project's enablement for the source plugin
/// either has no signal_allowlist (None = all signals) or the signal type
/// is in the allowlist.
pub fn is_signal_allowed(enablement: &PluginEnablement, signal_type: &str) -> bool {
    match &enablement.signal_allowlist {
        None => true, // None = all signal types allowed
        Some(list) => list.iter().any(|s| s == signal_type),
    }
}

// ── Signal Knowledge Capture (RFC 015 §"Signal Knowledge Capture") ──────────

/// Resolved capture policy for a signal delivered to a specific project.
///
/// Combines the plugin's declared defaults with the project's per-enablement
/// `SignalCaptureOverride`.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResolvedCapturePolicy {
    /// Whether to project this signal into cairn-graph as a GraphNode(Signal).
    pub graph_project: bool,
    /// Whether to ingest signal payload into cairn-memory.
    pub memory_ingest: bool,
}

/// Resolve the capture policy for a signal in a project.
///
/// Default behaviour (per RFC 015):
/// - graph_project defaults to `true` (cheap, useful for provenance)
/// - memory_ingest defaults to `false` (expensive, opt-in per plugin)
///
/// The plugin descriptor's `has_signal_source` and the per-enablement
/// `SignalCaptureOverride` compose: override takes precedence over default.
pub fn resolve_capture_policy(
    enablement: &PluginEnablement,
    plugin_declares_memory_ingest: bool,
) -> ResolvedCapturePolicy {
    let override_cfg = enablement.signal_capture_override.as_ref();

    let graph_project = override_cfg.and_then(|o| o.graph_project).unwrap_or(true); // default ON

    let memory_ingest = override_cfg
        .and_then(|o| o.memory_ingest)
        .unwrap_or(plugin_declares_memory_ingest); // default from plugin

    ResolvedCapturePolicy {
        graph_project,
        memory_ingest,
    }
}

// ── Errors ──────────────────────────────────────────────────────────────────

/// Marketplace-specific errors.
#[derive(Clone, Debug)]
pub enum MarketplaceError {
    PluginNotFound(String),
    PluginNotInstalled(String),
    InvalidTransition {
        plugin_id: String,
        from: String,
        to: String,
    },
    NotEnabledForProject {
        plugin_id: String,
        project: ProjectKey,
    },
    EvalScorerReserved,
}

impl std::fmt::Display for MarketplaceError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::PluginNotFound(id) => write!(f, "plugin not found: {id}"),
            Self::PluginNotInstalled(id) => write!(f, "plugin not installed: {id}"),
            Self::InvalidTransition {
                plugin_id,
                from,
                to,
            } => write!(
                f,
                "invalid state transition for plugin {plugin_id}: {from} -> {to}"
            ),
            Self::NotEnabledForProject { plugin_id, project } => write!(
                f,
                "plugin {plugin_id} not enabled for project {project:?}"
            ),
            Self::EvalScorerReserved => write!(
                f,
                "EvalScorer capability is reserved for a future RFC; v1 does not support plugin-provided eval scorers"
            ),
        }
    }
}

impl std::error::Error for MarketplaceError {}

