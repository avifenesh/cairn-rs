//! Plugin marketplace service — RFC 015.
//!
//! This is the layer above the existing plugin host (RFC 007). It manages:
//! - marketplace lifecycle (Listed → Installing → Installed → EnabledForProject → Uninstalled)
//! - per-project plugin enablement with tool/signal allowlists
//! - credential wizard flow
//! - bundled catalog loading
//!
//! There is NO `Connected` state and NO `POST /v1/plugins/:id/connect` endpoint.
//! Process instances are managed by the plugin host, not the marketplace.

use std::collections::{HashMap, HashSet};
use std::sync::Arc;
use std::time::{SystemTime, UNIX_EPOCH};

use cairn_domain::contexts::{PluginCategory, SignalCaptureOverride, VisibilityContext};
use cairn_domain::ids::{CredentialId, OperatorId, RunId, SignalId};
use cairn_domain::tenancy::ProjectKey;
use cairn_plugin_catalog::CatalogEntry;
use serde::{Deserialize, Serialize};

/// Opaque credential value — the encrypted/raw value provided by the operator.
/// RFC 015 §Commands specifies `CredentialValue`; currently a String wrapper.
pub type CredentialValue = String;

/// Graph node identifier — matches cairn-graph's GraphNode.node_id type.
pub type GraphNodeId = String;

// ── Plugin Descriptor (RFC 015 §"Plugin Descriptor") ─────────────────────────

/// Marketplace metadata wrapping the plugin manifest.
/// The manifest describes the plugin process; the descriptor wraps it
/// with discovery, credential, and health-check metadata for the marketplace.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct PluginDescriptor {
    /// Unique plugin identifier (must be unique per tenant).
    pub id: String,
    /// Human-readable plugin name.
    pub name: String,
    /// Plugin version string.
    pub version: String,
    /// Short description for the marketplace view.
    pub description: Option<String>,
    /// Marketplace filter category.
    pub category: PluginCategory,
    /// Icon URL displayed in marketplace view.
    pub icon_url: Option<String>,
    /// Vendor name ("GitHub", "Slack", "Custom").
    pub vendor: String,
    /// Binary command to spawn the plugin process.
    pub command: Vec<String>,
    /// Declared tool names this plugin provides.
    pub tools: Vec<String>,
    /// Declared signal source types this plugin emits.
    pub signal_sources: Vec<String>,
    /// Declared channel names this plugin can deliver to.
    pub channels: Vec<String>,
    /// Credentials the operator must provide (drives the wizard).
    pub required_credentials: Vec<CredentialSpec>,
    /// Declared network egress endpoints (stored+displayed in v1, NOT enforced).
    pub required_network_egress: Vec<String>,
    /// Optional health check for install/verify time.
    pub post_install_health_check: Option<HealthCheckSpec>,
    /// Where this descriptor came from.
    pub source: DescriptorSource,
    /// Download URL for the plugin binary (if from bundled catalog or remote).
    pub download_url: Option<String>,
    /// Whether this plugin declares any SignalSource capabilities
    /// (drives eager vs lazy spawn policy).
    pub has_signal_source: bool,
}

/// A credential the plugin requires, driving the credential wizard.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CredentialSpec {
    /// Internal key name (e.g. "github_app_private_key").
    pub key: String,
    /// Human-readable label for the wizard (e.g. "GitHub App Private Key").
    pub display_name: String,
    /// What kind of credential this is.
    pub kind: CredentialKind,
    /// URL with instructions for obtaining this credential.
    pub help_url: Option<String>,
    /// Optional pre-store validation.
    pub validation: Option<CredentialValidation>,
    /// The scope at which this credential is stored.
    pub scope: CredentialScopeHint,
    /// Whether this credential is auto-generated by cairn (e.g. webhook secrets).
    #[serde(default)]
    pub generated: bool,
}

/// Kind of credential.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CredentialKind {
    OAuth2,
    ApiKey,
    AppInstallation,
    BasicAuth,
}

/// Optional validation to run before storing a credential.
#[derive(Clone, Debug, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CredentialValidation {
    /// Call a URL and check for a 200 response.
    HttpCheck { url_template: String },
    /// Match a regex pattern.
    RegexMatch { pattern: String },
}

/// Scope hint for credential storage.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum CredentialScopeHint {
    Tenant,
    Workspace,
    Project,
}

/// Health check specification for install/verify time.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct HealthCheckSpec {
    /// RPC method on the plugin that proves connectivity.
    pub method: String,
    /// Hard timeout in milliseconds.
    pub timeout_ms: u64,
    /// JSON value that the response must match for success.
    pub success_criteria: serde_json::Value,
}

// ── Marketplace State ────────────────────────────────────────────────────────

/// Lifecycle state of a plugin in the marketplace (tenant-scoped).
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum MarketplaceState {
    Listed,
    Installing,
    Installed,
    InstallationFailed { reason: String },
    Uninstalled,
}

/// Where a plugin descriptor came from.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum DescriptorSource {
    BundledCatalog,
    LocalFile { path: String },
    RemoteUrl { url: String },
}

/// Credential scope key — derived from credential IDs bound to an enablement.
/// Process instances are keyed 1:1 with credential scope keys.
#[derive(Clone, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct CredentialScopeKey(pub String);

/// Reason a process instance became ready.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum InstanceReadyReason {
    EagerSignalSource,
    LazyFirstInvocation,
    Restart,
}

/// Reason a process instance stopped.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum InstanceStoppedReason {
    Drained,
    Failed { details: String },
    Uninstalled,
}

/// Outcome of an ephemeral credential verification.
#[derive(Clone, Debug, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum VerificationOutcome {
    Ok,
    Failed { reason: String },
}

